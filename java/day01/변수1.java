package day01;

public class 변수1 {

	public static void main(String[] args) {
		//ctrl + shift + f : 들여쓰기 정렬
		// 데이터 타입 : 기본형 (보라색, 주황색) or 참조형 (검은색, 보라색)
		int a = 10; // 정수
		char b = '가'; // 문자 하나
		String c = "문자열"; // 문자 여러개
		
		// 기본형 - 숫자(정수형) 주로 int형 사용
		
		byte byte1 = 1; // 8비트
		short short1 = 12345; // 16비트
		int int1 = 123456; // 32비트
		long long1 = 123456789L; // 64비트 숫자 끝에 롱타입 표시 (L)
		
		// 기본형 - 숫자(실수형) 주로 double형 사용
		
		float float1 = 12.34f; // 32비트 숫자 끝에 플로트형 표시 (f)
		double double1 = 1234.12345; // 64비트
		
		// 기본형 - 문자 1개 - 주로 참조형 String 사용
		
		char char1 = 'A'; // ' ' 문자 하나 사용, " " 문자 여러개 사용
		
		// 기본형 - 참, 거짓 (true, false) - 제어문 사용
		
		boolean bool1 = true;
		boolean bool2 = false;
		

	}

}
